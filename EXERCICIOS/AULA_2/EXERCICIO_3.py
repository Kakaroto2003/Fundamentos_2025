#Faça um programa que leia um número. Exiba o dia correspondente da semana.
#Observação: Resolva usando apenas Estruturas de Decisão Simples.

#Saída esperada:
#1 - Domingo,
#2 - Segunda,
#3 - Terça,
#4 - Quarta ... e assim por diante

dia = int(input("Digite o numero da semana: "))

if dia==1:
    print("Domingo")
if dia==2:
    print("Segunda")
if dia==3:
    print("Terca")
if dia==4:
    print("Quarta")
if dia==5:
    print("Quinta")
if dia==6:
    print("Sexta")
if dia==7:
    print("Sabado")